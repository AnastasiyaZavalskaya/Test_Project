function sqr() {
    var inputValue = document.getElementById("value").value;
    var result = Math.pow(inputValue, 2);
    alert(result);
}

function sqrt() {
    var inputValue = document.getElementById("value").value;
    var result = Math.sqrt(inputValue);
    alert(result);
}

function ln() {
    var inputValue = document.getElementById("value").value;
    var result = Math.log(inputValue);
    alert(result);
}
